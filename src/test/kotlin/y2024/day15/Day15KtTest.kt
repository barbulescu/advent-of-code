package y2024.day15

import org.assertj.core.api.Assertions.assertThat
import org.junit.jupiter.api.Test

class Day15KtTest {

    @Test
    fun `part 1 small example`() {
        val lines = """
            ########
            #..O.O.#
            ##@.O..#
            #...O..#
            #.#.O..#
            #...O..#
            #......#
            ########
            
            <^^>>>vv<v>>v<<
        """.trimIndent().lines()

        assertThat(lines.part1()).isEqualTo(2028)
    }

